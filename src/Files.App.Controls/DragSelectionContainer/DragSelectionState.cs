namespace Files.App.Controls
{
	/// <summary>
	/// Defines constants that specify the state of a drag selection.
	/// </summary>
	public enum DragSelectionState
	{
		/// <summary>
		/// The drag selection has started.
		/// </summary>
		Started,

		/// <summary>
		/// The drag selection is taking place.
		/// </summary>
		Moving,

		/// <summary>
		/// The drag selection has ended.
		/// </summary>
		Ended,
	}
}
