namespace Files.App.Controls;

/// <summary>
/// Defines the visibility behavior for the <see cref="SidebarView2"/> back button.
/// </summary>
public enum SidebarViewBackButtonVisible
{
	/// <summary>
	/// The back button is hidden.
	/// </summary>
	Collapsed,

	/// <summary>
	/// The back button is visible.
	/// </summary>
	Visible,

	/// <summary>
	/// The back button follows the default sidebar behavior.
	/// </summary>
	Auto,
}
